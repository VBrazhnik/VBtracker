package com.vb.tracker.free.agenda;

class Sample {

    private int id;
    private int type;

    public Sample() {
    }

    Sample(int id, int type) {
        this.id = id;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
